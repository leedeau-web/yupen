export function generateMockSurveyResponse(voter) {
  const candidate = voter.지지후보;
  const speech = voter.말투특성;
  const intensity = voter.지지강도;

  const pool = RESPONSES[candidate]?.[speech] ?? ["잘 모르겠어예..."];
  const response = pool[voter.id % pool.length];

  const basePersuasibility = candidate === "미정" ? 0.7 : (6 - intensity) / 5;
  const persuasibility = Math.max(0.05, Math.min(0.95, basePersuasibility + (Math.random() * 0.1 - 0.05)));

  const emotion = EMOTIONS[candidate]?.[speech] ?? "중립";

  return { response, emotion, persuasibility: Math.round(persuasibility * 100) / 100 };
}
